#Q-21 What are oops concepts? Is multiple inheritance supported in java?
''' OOP is a concept of bundling related and similar properties in a individual objects. Which also follows real-life relation.

The major OOP concepts are :

Encapsulation : Bundling data and method in a class is known as encapsulation.

Inheritance : It allows the subclass to inherit the properties of the superclass.

Polymorphism : The ability of data to take more than one form.

Abstraction : Concept displaying only the essential information and hiding the rest.

Java does not support multiple Inheritance. '''